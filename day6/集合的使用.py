# 创建集合
s={1,2,3,4}
s1=set((1,2,3))
s2=set()
s3=set((1,))   #
print(s)
print(s1)
print(s2)
print(s3)

set_1=set([12,34,6,2,12])
print(set_1)   #{34, 2, 12, 6}

set_2=set({'a':1,'b':2,'c':3})
print(set_2)   #{'c', 'a', 'b'}
